package com.aspire.java.oops.encapsulation;

// getter setter method

public class Define
{
	private String name;
	private int mobNo;
	
	public void setName(String assignedName)
	{
		name = assignedName;
	}
	public void setMobNo(int assignedMobNo)
	{
		mobNo = assignedMobNo;
	}
	public String getName()
	{
		return name;
	}
	public int getMobNo()
	{
		return mobNo;
	}
}
