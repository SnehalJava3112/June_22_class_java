package com.aspire.java.oops.polymorphism;

public class OverridingParent	//Parent class
{
	public void jwellary()
	{
		System.out.println("Parent Jwellary");
	}
	public void farm()
	{
		System.out.println("Parent Farm");
	}
	public void bunglow()
	{
		System.out.println("Parent Bunglow");
	}
	public void addition(int a, int b)
	{
		int sum = a+b;
		System.out.println(sum);
	}
}
